    @Override
    public void onEvent(String name, String params, byte[] data, int offset, int length) {
        String logTxt = "name: " + name;

        if (params != null && !params.isEmpty()) {
            logTxt += " ;params :" + params;
        }
        if (name.equals("asr.partial")) {
            try {
                jsonObject = new JSONObject(params);
                final_result = jsonObject.getString("best_result");
            } catch (JSONException e) {
                e.printStackTrace();
            }
        }
        if (name.equals("asr.finish")) {
            if(final_result.equals("")){
                return;
            }
            switch (final_result){
                case "我做了什么":
                    TEXT = "主人创造了我，使我能感受全新的世界";
                    break;
                case "我怎么样":
                    TEXT = "主人很好，和我一样喜欢学习";
                    break;
                case "你怎么样":
                    TEXT = "我很乖，喜欢一直陪伴主人";
                    break;
                case "我的家人怎么样":
                    TEXT = "他们像你爱他们一样，深深地爱着你";
                    break;
                case "我能做什么":
                    TEXT = "广阔天地，大有作为，主人加油";
                    break;
                case "解除连接":
                    txtResult.setText("");
                    TEXT = "那主人记得下次找我聊天";
                    isExit = true;
                    break;
                default:
                    TEXT = maidName + "不知道怎么回答主人，好伤心";
                    break;
            }
            stopSTW();
            txtResult.setText(final_result);
            txtLog.append("主人：" + final_result + "\n");
            final_result = "";
            new Handler().postDelayed(new Runnable(){
                public void run() {
                    speak();
                }
            }, 500);
        }
        printLog(logTxt);
    }

